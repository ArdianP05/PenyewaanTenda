    public void hapusDataKategori(){
        loadDataKategori(); 
        
        int pesan = JOptionPane.showConfirmDialog(null, "HAPUS DATA"+ " " + Kategori +"?","KONFIRMASI", JOptionPane.OK_CANCEL_OPTION);
        
        if(pesan == JOptionPane.OK_OPTION){
            try{
                Statement stat = (Statement) koneksi.getKoneksi().createStatement();
                
                String sql = "DELETE FROM tbl_kategori WHERE kode_kategori='"+ Kategori +"'";
                PreparedStatement p =(PreparedStatement)koneksi.getKoneksi().prepareStatement(sql);
                p.executeUpdate();
                
                getDataKategori();
                
                reset();
                JOptionPane.showMessageDialog(null, "DATA KATEGORI TENDA BERHASIL DIHAPUS");
            }catch(SQLException err){
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        }
    }
