    public void loadDataKategori(){
        Kategori = ekode.getText();
        jenis = ejenis.getText();
        }
    
    public void dataSelect(){
        int i = tblKategori.getSelectedRow();
        
        if(i == -1){
            return;
        }
        ekode.setText(""+model.getValueAt(i,0));
        ejenis.setText(""+model.getValueAt(i,1));
       }
     
    public void reset(){
        Kategori = "";
        jenis = "";

               
        ekode.setText(Kategori);
        ejenis.setText(jenis);
    }
    
    public void simpanDataKategori(){
        loadDataKategori();
        
        try{
            Statement stat = (Statement) koneksi.getKoneksi().createStatement();
            
            String  sql =   "INSERT INTO tbl_kategori(kode_kategori, jenis_kategori)" + "VALUES('"+ Kategori +"','"+ jenis +"')";
            PreparedStatement p = (PreparedStatement) koneksi.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            getDataKategori();
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
    public void perbaruiDataKategori(){
        loadDataKategori();
        
        try{
            Statement stat = (Statement) koneksi.getKoneksi().createStatement();
            
            String sql = "UPDATE tbl_kategori SET jenis_kategori = '"+ jenis +"' WHERE kode_kategori = '"+ Kategori +"'";
            PreparedStatement p =(PreparedStatement)koneksi.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            getDataKategori();
            
            reset();
            JOptionPane.showMessageDialog(null, "PERUBAHAN DATA BERHASIL");
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
    
